"""
DOCX renderer (Pass 3).

Converts authored markdown content into a board-packet-quality Word
document using python-docx. The renderer's external contract is the
same as the other renderers in this package:

    render(content, title, include_audit_footer, audit_metadata) -> bytes

Bytes are returned (not a file path) so the storage backend remains
responsible for persistence. This separation lets cloud backends
(Google Drive, OneDrive, SharePoint in v2) upload the same bytes
without changes here.

Design choices (locked in Pass 3 design discussion):
  - Cover page when title is provided (title, optional status/audience
    from audit_metadata, generated date, "Generated by ADAM", page break)
  - Mirror the HTML renderer's markdown coverage: H1-H6, paragraphs with
    inline bold/italic/code, bulleted/numbered lists, pipe tables,
    fenced code blocks, horizontal rules, links
  - End-of-document audit footer (NOT a repeating page footer, per v1
    decision -- page-footer mode can be added later as an opt-in arg)
  - Page numbers in the standard footer (separate from audit metadata)
  - Document properties always populated: title, author, subject,
    comments, keywords -- so the file is self-describing when detached
    from ADAM's logs
  - Calibri 11pt body, 1-inch margins, headings styled for visual hierarchy
  - Tables have a shaded header row
  - Code blocks render as monospace paragraphs with a light gray background
  - Links render as blue underlined text and a real Word hyperlink when
    the URL is well-formed

Governance preserved:
  - Renderer produces bytes only -- no file paths, no network, no
    arbitrary I/O
  - The handler enforces filename sanitization and content size limits
    before this code runs
"""

import io
import re
from datetime import datetime
from typing import Any, Dict, List, Optional, Tuple

# python-docx imports. We import at module load (not lazily inside
# render()) so a missing dependency surfaces clearly during skill
# discovery rather than at first invocation. requirements.txt already
# pins python-docx; if it's missing the dependency check at startup
# warns about it.
from docx import Document
from docx.enum.table import WD_ALIGN_VERTICAL
from docx.enum.text import WD_ALIGN_PARAGRAPH, WD_BREAK
from docx.oxml.ns import qn
from docx.oxml import OxmlElement
from docx.shared import Cm, Inches, Pt, RGBColor


# ============================================================
# Styling constants
# ============================================================

# Body font + size. Calibri 11 is the cross-platform-safe choice for
# board-facing documents. Aptos (Word's newer default) is fine if it
# resolves, but falls back to Calibri otherwise.
BODY_FONT_NAME    = "Calibri"
BODY_FONT_SIZE_PT = 11

# Code-block font: monospace, slightly smaller so blocks don't dominate
# visually. Consolas is Word's standard monospace; falls back to
# Courier New on systems without it.
CODE_FONT_NAME    = "Consolas"
CODE_FONT_SIZE_PT = 10

# Colors. RGBColor takes ints, not hex strings.
COLOR_HEADING_2_BORDER = RGBColor(0xBF, 0xBF, 0xBF)  # subtle grey
COLOR_TABLE_HEADER_BG  = "EEEEEE"                    # used as hex in XML
COLOR_CODE_BG          = "F5F5F5"                    # used as hex in XML
COLOR_LINK             = RGBColor(0x05, 0x63, 0xC1)  # Word's hyperlink blue
COLOR_AUDIT_FOOTER     = RGBColor(0x80, 0x80, 0x80)  # mid-grey
COLOR_COVER_SUBTLE     = RGBColor(0x80, 0x80, 0x80)


# ============================================================
# Helpers for low-level XML manipulation
# ============================================================
#
# python-docx exposes most of what we need through its high-level API,
# but a few details (table cell shading, paragraph borders, real
# hyperlinks, page numbers) require dropping to the underlying OOXML.
# We isolate the XML-fiddling in these helpers so the main render
# functions stay readable.

def _set_cell_shading(cell, fill_hex: str) -> None:
    """Apply background shading to a table cell."""
    tc_pr = cell._tc.get_or_add_tcPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:val"), "clear")
    shd.set(qn("w:color"), "auto")
    shd.set(qn("w:fill"), fill_hex)
    tc_pr.append(shd)


def _set_paragraph_shading(paragraph, fill_hex: str) -> None:
    """Apply background shading to a paragraph (used for code blocks)."""
    p_pr = paragraph._p.get_or_add_pPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:val"), "clear")
    shd.set(qn("w:color"), "auto")
    shd.set(qn("w:fill"), fill_hex)
    p_pr.append(shd)


def _set_paragraph_bottom_border(paragraph, color_hex: str = "BFBFBF",
                                  size: str = "6") -> None:
    """Add a bottom border to a paragraph (used for hr and H2 styling)."""
    p_pr = paragraph._p.get_or_add_pPr()
    p_bdr = OxmlElement("w:pBdr")
    bottom = OxmlElement("w:bottom")
    bottom.set(qn("w:val"), "single")
    bottom.set(qn("w:sz"), size)
    bottom.set(qn("w:space"), "1")
    bottom.set(qn("w:color"), color_hex)
    p_bdr.append(bottom)
    p_pr.append(p_bdr)


def _add_page_field(paragraph) -> None:
    """
    Insert a PAGE field into a paragraph. Used to put page numbers in
    the footer. This is the OOXML pattern for "dynamic field that Word
    evaluates at render time."
    """
    run = paragraph.add_run()
    fld_char_begin = OxmlElement("w:fldChar")
    fld_char_begin.set(qn("w:fldCharType"), "begin")
    instr_text = OxmlElement("w:instrText")
    instr_text.set(qn("xml:space"), "preserve")
    instr_text.text = "PAGE"
    fld_char_end = OxmlElement("w:fldChar")
    fld_char_end.set(qn("w:fldCharType"), "end")
    run._r.append(fld_char_begin)
    run._r.append(instr_text)
    run._r.append(fld_char_end)


def _add_hyperlink(paragraph, text: str, url: str) -> None:
    """
    Add a real Word hyperlink (clickable in Word and downstream systems).
    Renders as blue underlined text. If the URL is malformed and
    relationship creation fails, we degrade gracefully to a styled run
    so the visible text is preserved.
    """
    try:
        part = paragraph.part
        r_id = part.relate_to(
            url,
            "http://schemas.openxmlformats.org/officeDocument/2006/relationships/hyperlink",
            is_external=True,
        )
    except Exception:
        # Malformed URL or relationship failure: just render as styled text
        run = paragraph.add_run(text)
        run.font.color.rgb = COLOR_LINK
        run.font.underline = True
        return

    hyperlink = OxmlElement("w:hyperlink")
    hyperlink.set(qn("r:id"), r_id)

    new_run = OxmlElement("w:r")
    r_pr = OxmlElement("w:rPr")
    # Blue color
    color = OxmlElement("w:color")
    color.set(qn("w:val"), "0563C1")
    r_pr.append(color)
    # Underline
    underline = OxmlElement("w:u")
    underline.set(qn("w:val"), "single")
    r_pr.append(underline)
    new_run.append(r_pr)
    text_el = OxmlElement("w:t")
    text_el.set(qn("xml:space"), "preserve")
    text_el.text = text
    new_run.append(text_el)
    hyperlink.append(new_run)

    paragraph._p.append(hyperlink)


# ============================================================
# Inline markdown -> Word runs
# ============================================================
#
# We hand-roll inline parsing rather than depend on a markdown library
# so the renderer stays stdlib + python-docx only. The supported
# patterns mirror the HTML renderer:
#   `code`               -> monospace run
#   **bold**             -> bold run
#   __bold__             -> bold run
#   *italic*             -> italic run (no mid-word matches)
#   _italic_             -> italic run (no mid-word matches)
#   [text](url)          -> real hyperlink
# Anything not matching is added as plain text.

# Inline tokenizer: ordered list of (pattern, kind) tuples. Order
# matters -- code spans first so their contents aren't interpreted as
# bold/italic; then links; then bold; then italic.
_INLINE_PATTERNS: List[Tuple[re.Pattern, str]] = [
    (re.compile(r"`([^`]+)`"),                                 "code"),
    (re.compile(r"\[([^\]]+)\]\(([^)]+)\)"),                   "link"),
    (re.compile(r"\*\*([^*]+)\*\*"),                            "bold"),
    (re.compile(r"__([^_]+)__"),                                "bold"),
    (re.compile(r"(?<!\w)\*([^*\n]+)\*(?!\w)"),                 "italic"),
    (re.compile(r"(?<!\w)_([^_\n]+)_(?!\w)"),                   "italic"),
]


def _tokenize_inline(text: str) -> List[Tuple[str, Any]]:
    """
    Tokenize a line into ('text', str), ('code', str), ('bold', str),
    ('italic', str), or ('link', (text, url)) tuples.

    Strategy: scan the text once, repeatedly finding the earliest match
    across all patterns. Whatever sits before the earliest match is a
    plain-text token; the match itself becomes a styled token; then we
    continue from the end of the match.
    """
    tokens: List[Tuple[str, Any]] = []
    pos = 0
    while pos < len(text):
        earliest: Optional[Tuple[int, re.Match, str]] = None
        for pattern, kind in _INLINE_PATTERNS:
            m = pattern.search(text, pos)
            if m and (earliest is None or m.start() < earliest[0]):
                earliest = (m.start(), m, kind)
        if earliest is None:
            tokens.append(("text", text[pos:]))
            break
        start, match, kind = earliest
        if start > pos:
            tokens.append(("text", text[pos:start]))
        if kind == "link":
            tokens.append(("link", (match.group(1), match.group(2))))
        else:
            tokens.append((kind, match.group(1)))
        pos = match.end()
    return tokens


def _add_inline_to_paragraph(paragraph, text: str) -> None:
    """Render inline markdown into runs on the given paragraph."""
    for kind, value in _tokenize_inline(text):
        if kind == "link":
            link_text, url = value
            _add_hyperlink(paragraph, link_text, url)
            continue
        run = paragraph.add_run(value)
        if kind == "code":
            run.font.name = CODE_FONT_NAME
            run.font.size = Pt(CODE_FONT_SIZE_PT)
        elif kind == "bold":
            run.bold = True
        elif kind == "italic":
            run.italic = True
        # 'text' is plain; no styling needed


