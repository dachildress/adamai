def _set_base_styles(doc: Document) -> None:
    """Set the Normal style font + size for the whole document."""
    normal = doc.styles["Normal"]
    normal.font.name = BODY_FONT_NAME
    normal.font.size = Pt(BODY_FONT_SIZE_PT)
    # 1-inch margins on all sides
    for section in doc.sections:
        section.top_margin    = Inches(1)
        section.bottom_margin = Inches(1)
        section.left_margin   = Inches(1)
        section.right_margin  = Inches(1)


def _add_page_number_footer(doc: Document) -> None:
    """Place 'Page X' in the default section footer."""
    for section in doc.sections:
        footer = section.footer
        # If the footer already has a paragraph (it always does in
        # python-docx-created docs), use it; otherwise create one.
        para = footer.paragraphs[0] if footer.paragraphs else footer.add_paragraph()
        para.alignment = WD_ALIGN_PARAGRAPH.CENTER
        para.text = ""  # clear any default
        run = para.add_run("Page ")
        run.font.size = Pt(9)
        _add_page_field(para)


def _populate_core_properties(
    doc:        Document,
    title:      Optional[str],
    audit_meta: Optional[Dict[str, Any]],
) -> None:
    """
    Always-populate document properties so the file is self-describing
    when opened outside ADAM's log directory.
    """
    cp = doc.core_properties
    cp.title    = title or "Untitled ADAM artifact"
    cp.author   = "ADAM (Adaptive Dynamic Agent Multiplex)"
    cp.keywords = "ADAM, deliberation, audit-grade"
    if audit_meta:
        session_id    = str(audit_meta.get("session_id", "")) or "unknown"
        invocation_id = str(audit_meta.get("invocation_id", "")) or "unknown"
        artifact_id   = str(audit_meta.get("artifact_id", "")) or "unknown"
        generated     = str(audit_meta.get("generated", "")) or "unknown"
        fmt           = str(audit_meta.get("format", "docx"))
        cp.subject = f"ADAM-generated artifact ({fmt}) - session {session_id}"
        cp.comments = (
            f"invocation_id={invocation_id} | "
            f"artifact_id={artifact_id} | "
            f"generated={generated}"
        )
    else:
        cp.subject  = "ADAM-generated artifact"
        cp.comments = ""
    # python-docx's CoreProperties doesn't expose 'category' as a
    # setter on all versions, so we set it via the underlying element
    # when accessible. Failing silently is fine -- category is nice-
    # to-have, not load-bearing for board readability.
    try:
        cp.category = "ADAM Artifact"
    except AttributeError:
        pass


def _add_cover_page(
    doc:        Document,
    title:      str,
    audit_meta: Optional[Dict[str, Any]],
) -> None:
    """
    Add a cover page. Called only when title is provided.
    Layout (top-to-bottom):
      - vertical space
      - Title (large, centered)
      - Optional status line from audit_meta.status
      - Optional audience line from audit_meta.audience (if set)
      - vertical space
      - Generated date
      - "Generated by ADAM"
      - hard page break
    """
    # Top spacing: a few empty paragraphs push the title down to roughly
    # the upper third of the page. Could be done with section spacing,
    # but blank paragraphs are simpler and work consistently.
    for _ in range(6):
        doc.add_paragraph()

    title_para = doc.add_paragraph()
    title_para.alignment = WD_ALIGN_PARAGRAPH.CENTER
    title_run = title_para.add_run(title)
    title_run.bold = True
    title_run.font.size = Pt(28)
    title_run.font.name = BODY_FONT_NAME

    # Status subtitle (e.g. "Status: Draft") if present
    status = (audit_meta or {}).get("status")
    if status:
        sub_para = doc.add_paragraph()
        sub_para.alignment = WD_ALIGN_PARAGRAPH.CENTER
        sub_run = sub_para.add_run(f"Status: {status}")
        sub_run.italic = True
        sub_run.font.size = Pt(14)
        sub_run.font.color.rgb = COLOR_COVER_SUBTLE

    # Audience line if present
    audience = (audit_meta or {}).get("audience")
    if audience:
        aud_para = doc.add_paragraph()
        aud_para.alignment = WD_ALIGN_PARAGRAPH.CENTER
        aud_run = aud_para.add_run(f"Prepared for: {audience}")
        aud_run.italic = True
        aud_run.font.size = Pt(12)
        aud_run.font.color.rgb = COLOR_COVER_SUBTLE

    # More vertical space before the bottom block
    for _ in range(8):
        doc.add_paragraph()

    # Generated date
    generated = (audit_meta or {}).get("generated", "")
    if generated:
        date_para = doc.add_paragraph()
        date_para.alignment = WD_ALIGN_PARAGRAPH.CENTER
        date_run = date_para.add_run(f"Generated: {generated}")
        date_run.font.size = Pt(11)
        date_run.font.color.rgb = COLOR_COVER_SUBTLE

    # "Generated by ADAM"
    byline = doc.add_paragraph()
    byline.alignment = WD_ALIGN_PARAGRAPH.CENTER
    byline_run = byline.add_run("Generated by ADAM (Adaptive Dynamic Agent Multiplex)")
    byline_run.font.size = Pt(11)
    byline_run.font.color.rgb = COLOR_COVER_SUBTLE
    byline_run.italic = True

    # Hard page break to end the cover page
    break_para = doc.add_paragraph()
    break_para.add_run().add_break(WD_BREAK.PAGE)


def _add_heading(doc: Document, level: int, text: str) -> None:
    """
    Add a heading at the given level. python-docx's built-in 'Heading 1'
    through 'Heading 9' styles provide the right visual hierarchy.
    Level 2 gets a subtle bottom border for extra visual separation
    in board-facing documents.
    """
    level = max(1, min(level, 6))
    heading = doc.add_heading(level=level)
    _add_inline_to_paragraph(heading, text.strip())
    if level == 2:
        _set_paragraph_bottom_border(heading, color_hex="BFBFBF", size="4")


def _add_paragraph(doc: Document, text: str) -> None:
    """Add a normal paragraph with inline markdown rendered."""
    para = doc.add_paragraph()
    _add_inline_to_paragraph(para, text)


def _add_bullet(doc: Document, text: str, level: int = 0) -> None:
    """Add a bulleted-list item at the given nesting level."""
    # python-docx exposes 'List Bullet', 'List Bullet 2', 'List Bullet 3'
    # for nesting levels 1, 2, 3. Levels deeper than that fall back to
    # level 3 since deeper styles aren't reliably present in default templates.
    style = "List Bullet" if level <= 0 else f"List Bullet {min(level + 1, 3)}"
    try:
        para = doc.add_paragraph(style=style)
    except KeyError:
        para = doc.add_paragraph(style="List Bullet")
    _add_inline_to_paragraph(para, text)


def _add_numbered(doc: Document, text: str, level: int = 0) -> None:
    """Add a numbered-list item at the given nesting level."""
    style = "List Number" if level <= 0 else f"List Number {min(level + 1, 3)}"
    try:
        para = doc.add_paragraph(style=style)
    except KeyError:
        para = doc.add_paragraph(style="List Number")
    _add_inline_to_paragraph(para, text)


def _add_code_block(doc: Document, code: str) -> None:
    """Render a fenced code block as a monospace paragraph with light
    grey background. Newlines inside the block become line breaks."""
    para = doc.add_paragraph()
    _set_paragraph_shading(para, COLOR_CODE_BG)
    lines = code.split("\n")
    for i, line in enumerate(lines):
        run = para.add_run(line)
        run.font.name = CODE_FONT_NAME
        run.font.size = Pt(CODE_FONT_SIZE_PT)
        if i < len(lines) - 1:
            run.add_break()


def _add_hr(doc: Document) -> None:
    """Render a horizontal rule as an empty paragraph with a bottom border."""
    para = doc.add_paragraph()
    _set_paragraph_bottom_border(para, color_hex="BFBFBF", size="6")


def _add_table(doc: Document, header_cells: List[str],
                body_rows: List[List[str]]) -> None:
    """
    Render a markdown pipe-style table as a Word table with a shaded,
    bold header row.
    """
    if not header_cells:
        return
    # Make all rows the same width as the header (pad short rows, trim
    # over-long rows so the visual layout stays predictable).
    width = len(header_cells)
    normalized_body = []
    for row in body_rows:
        if len(row) < width:
            row = row + [""] * (width - len(row))
        elif len(row) > width:
            row = row[:width]
        normalized_body.append(row)

    table = doc.add_table(rows=1 + len(normalized_body), cols=width)
    table.style = "Light Grid Accent 1"  # built-in style with visible borders
    table.autofit = True

    # Header
    header_row = table.rows[0]
    for i, cell_text in enumerate(header_cells):
        cell = header_row.cells[i]
        _set_cell_shading(cell, COLOR_TABLE_HEADER_BG)
        # Clear default paragraph, add our own with bold inline content
        cell.text = ""
        para = cell.paragraphs[0]
        run = para.add_run("")
        # Render inline (handles **bold** if author put it in headers)
        _add_inline_to_paragraph(para, cell_text.strip())
        # Force header text to bold regardless of inline state
        for r in para.runs:
            r.bold = True
        cell.vertical_alignment = WD_ALIGN_VERTICAL.CENTER

    # Body
    for row_idx, row in enumerate(normalized_body, start=1):
        for col_idx, cell_text in enumerate(row):
            cell = table.rows[row_idx].cells[col_idx]
            cell.text = ""
            para = cell.paragraphs[0]
            _add_inline_to_paragraph(para, cell_text.strip())


def _add_audit_footer(doc: Document, audit_meta: Dict[str, Any]) -> None:
    """
    Append an end-of-document audit metadata block. Per Pass 3 decision,
    this is NOT a repeating page footer -- it's a styled block at the
    very end of the document, after the main content.
    """
    # Visual separator: bordered empty paragraph
    sep = doc.add_paragraph()
    _set_paragraph_bottom_border(sep, color_hex="BFBFBF", size="6")

    # Header
    header_para = doc.add_paragraph()
    header_run = header_para.add_run("Audit metadata")
    header_run.bold = True
    header_run.font.color.rgb = COLOR_AUDIT_FOOTER
    header_run.font.size = Pt(10)

    # Per-key lines
    for key, value in audit_meta.items():
        line = doc.add_paragraph()
        run = line.add_run(f"{key}: {value}")
        run.font.color.rgb = COLOR_AUDIT_FOOTER
        run.font.size = Pt(9)
        run.italic = True


# ============================================================
# Block-level markdown parser
# ============================================================
#
# We re-walk the content line by line and dispatch to the appropriate
# add_* helper. This mirrors the HTML renderer's block dispatcher; the
# only differences are the output target (a Word document via python-
# docx) and the styling decisions baked into the helpers above.

def _parse_and_render_blocks(doc: Document, content: str) -> None:
    """Walk the markdown content and render each block into the doc."""
    lines = content.splitlines()
    i = 0
    para_buffer: List[str] = []

    def flush_paragraph() -> None:
        if para_buffer:
            joined = " ".join(line.strip() for line in para_buffer)
            _add_paragraph(doc, joined)
            para_buffer.clear()

    while i < len(lines):
        line = lines[i]

        # Code fence: ``` to ```
        if line.strip().startswith("```"):
            flush_paragraph()
            code_lines: List[str] = []
            i += 1
            while i < len(lines) and not lines[i].strip().startswith("```"):
                code_lines.append(lines[i])
                i += 1
            _add_code_block(doc, "\n".join(code_lines))
            i += 1
            continue

        # Header
        m = re.match(r"^(#{1,6})\s+(.*)$", line)
        if m:
            flush_paragraph()
            level = len(m.group(1))
            _add_heading(doc, level, m.group(2).strip())
            i += 1
            continue

        # Horizontal rule
        if re.match(r"^[-*_]{3,}\s*$", line):
            flush_paragraph()
            _add_hr(doc)
            i += 1
            continue

        # Unordered list (with simple nesting based on leading spaces)
        if re.match(r"^\s*[-*+]\s+", line):
            flush_paragraph()
            while i < len(lines) and re.match(r"^\s*[-*+]\s+", lines[i]):
                lead = len(lines[i]) - len(lines[i].lstrip())
                level = lead // 2  # 2 spaces per nesting level (CommonMark-ish)
                item_text = re.sub(r"^\s*[-*+]\s+", "", lines[i])
                _add_bullet(doc, item_text, level=level)
                i += 1
            continue

        # Ordered list
        if re.match(r"^\s*\d+\.\s+", line):
            flush_paragraph()
            while i < len(lines) and re.match(r"^\s*\d+\.\s+", lines[i]):
                lead = len(lines[i]) - len(lines[i].lstrip())
                level = lead // 3  # numbered lists indent more naturally
                item_text = re.sub(r"^\s*\d+\.\s+", "", lines[i])
                _add_numbered(doc, item_text, level=level)
                i += 1
            continue

        # Table: pipe-style with header separator on next line
        if ("|" in line and i + 1 < len(lines)
                and re.match(r"^\s*\|?[-:\s|]+\|?\s*$", lines[i + 1])):
            flush_paragraph()
            header = [c.strip() for c in line.strip().strip("|").split("|")]
            i += 2  # skip the separator
            body: List[List[str]] = []
            while i < len(lines) and "|" in lines[i] and lines[i].strip():
                row = [c.strip() for c in lines[i].strip().strip("|").split("|")]
                body.append(row)
                i += 1
            _add_table(doc, header, body)
            continue

        # Blank line -> paragraph break
        if not line.strip():
            flush_paragraph()
            i += 1
            continue

        # Default: accumulate into a paragraph
        para_buffer.append(line.strip())
        i += 1

    flush_paragraph()


# ============================================================
# Public render() entrypoint
# ============================================================

def render(
    content:              str,
    title:                Optional[str]            = None,
    include_audit_footer: bool                     = True,
    audit_metadata:       Optional[Dict[str, Any]] = None,
) -> bytes:
    """
    Render markdown content as a board-packet-quality .docx.

    Args:
        content:              The authored markdown content.
        title:                Optional document title. When provided, a
                              cover page is generated.
        include_audit_footer: When True (default), an end-of-document
                              audit metadata block is appended.
        audit_metadata:       Dict of audit fields (session_id,
                              invocation_id, artifact_id, generated,
                              format, status, audience, etc.). Used
                              for both the cover page (status, audience)
                              and the end-of-document audit footer, and
                              copied into Word document properties.

    Returns:
        The .docx file content as bytes, ready for the storage backend
        to write.
    """
    doc = Document()

    _set_base_styles(doc)
    _populate_core_properties(doc, title=title, audit_meta=audit_metadata)
    _add_page_number_footer(doc)

    # Cover page (only if a title was provided -- per Pass 3 decision,
    # we don't fabricate a cover page when the caller didn't ask for one)
    if title:
        _add_cover_page(doc, title=title, audit_meta=audit_metadata)

    # Main content
    _parse_and_render_blocks(doc, content)

    # End-of-document audit block
    if include_audit_footer and audit_metadata:
        _add_audit_footer(doc, audit_metadata)

    # Serialize to bytes via in-memory buffer (no filesystem touch here;
    # the storage backend owns persistence)
    buf = io.BytesIO()
    doc.save(buf)
    return buf.getvalue()
